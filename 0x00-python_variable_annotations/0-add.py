#!/usr/bin/env python3
"""
Write a type-annotated function `add`.
    Function takes 2 agruments:
        1. `a` of float type.
        2. `b` of float type.
    Function returns the sum of both numbers, return type float
"""


def add(a: float, b: float) -> float:
    """Sums two float numbers together."""
    return a + b
