#!/usr/bin/env python3
"""
Script contains a type-annotated function `concat`.
    Function takes two arguments:
        1. `str1` of string type.
        2. `str2` of string type.
    Function returns a concatenated string.
"""


def concat(str1: str, str2: str) -> str:
    """Concantenates two strings together."""
    return str1 + str2
